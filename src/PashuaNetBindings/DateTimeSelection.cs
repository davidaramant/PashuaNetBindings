namespace Pashua
{
    /// <summary>
    /// Which types of timestamps the user is allowed to select.
    /// </summary>
    public enum DateTimeSelection
    {
        /// <summary>
        /// Only times.
        /// </summary>
        TimeOnly,
        /// <summary>
        /// Only dates.
        /// </summary>
        DateOnly,
        /// <summary>
        /// Both a date and time.
        /// </summary>
        BothTimeAndDate,
    }
}
